for num in range(101):
    if num % 10 == 0:
        if num == 0:
            print("zero")
        elif num == 10:
            print("ten")
        elif num == 20:
            print("twenty")
        elif num == 30:
            print("thirty")
        elif num == 40:
            print("forty")
        elif num == 50:
            print("fifty")
        elif num == 60:
            print("sixty")
        elif num == 70:
            print("seventy")
        elif num == 80:
            print("eighty")
        elif num == 90:
            print("ninety")
        elif num == 100:
            print("One-Hundred")
    else:
        print(num)
